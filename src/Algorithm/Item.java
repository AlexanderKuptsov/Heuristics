package Algorithm;

public class Item {

    private float weight;
    private float cost;

    public Item(float weight, float cost) {
        this.weight = weight;
        this.cost = cost;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public float getCost() {
        return cost;
    }

    public void setCost(float cost) {
        this.cost = cost;
    }
}
